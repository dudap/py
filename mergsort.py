def merge(L,start,center,finish):
    i=start
    j=center+1
    L2=[]

    while (i<=center) and (j<=finish):
        if L[j]<L[i]:
            L2.append(L[j])
            j+=1
        else:
            L2.append(L[i])
            i+=1
    if i<=center:
        while i<=center:
            L2.append(L[i])
            i+=1
    else:
        while j<=finish:
            L2.append(L[j])
            j+=1

    s=finish-start+1
    i=0
    while i<s:
        L[start+i]=L2[i]
        i+=1
    return L





def msort(L,start,finish):
    if start!=finish:
        center=int(math.floor((start+finish)/2))

        msort(L,start,center)
        msort(L,center+1,finish)
        merge(L,start,center,finish)
    return L

